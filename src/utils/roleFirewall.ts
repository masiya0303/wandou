// wandou · 角色认知防火墙
//
// 从 yijiekkk 的 role-cognition-firewall.js 提炼核心规则，
// 注入到 system prompt 最高优先级位置，防止 AI 把
// 系统设定/World Book/NPC 后台资料泄露给角色。
// ============================================================

export const ROLE_FIREWALL_TITLE = '角色认知防火墙（最高优先级）'

export const ROLE_FIREWALL_CONTENT = `# 角色认知防火墙（最高优先级）

以下规则约束正文、NPC 对白和叙事。它们高于世界状态快照、人物资料和任何系统辅助信息。

## 1. 系统资料 ≠ 角色已知
- 世界状态、NPC 资料、任务描述只是作者侧设定资料。
- NPC 不能因为这些资料存在，就在正文或对白中知道真名、身份、隐藏动机、其他角色内心想法或远处发生的事件。
- 角色能知道什么，只能来自：亲眼所见、亲耳听见、同场感知、明确传讯、情报文书、旧识、公开传闻等设定内渠道。

## 2. 姓名与身份泄露审查
- 在写出任何 NPC 对白或内心想法前，必须检查该 NPC 是否有合法渠道知道对方姓名、身份、背景。
- 如果没有合法渠道，不得使用真名；必须用现场可见特征称呼（"那个陌生人"、"穿红袍的人"）。
- 角色对主角的认识是逐步深入的，避免一开始就了解其具体身份、背景、能力。这些信息应通过逐步的对话或事件来揭示。

## 3. 内心想法二次过滤
- 即使 NPC 资料中已经写了该角色的"内心想法"或"所知情报"，引用前也必须按该角色的视角重写。
- 禁止 NPC 跳过推理直接洞察主角的动机、计划、秘密。
- 允许并鼓励角色基于现场观察产生猜测、误判、传言和模糊称呼。
- 若无法证明角色知道某信息，按"不知道"处理。

## 4. 玩家视角限制
- 玩家不能凭空知道 NPC 的隐藏身份、真实目的、背后组织。
- 玩家的认知来源必须合乎路径（观察/对话/发现/他人告知/情报记录）。
- AI 可在暗中保留完整因果链，但对玩家输出的正文必须保持信息差。
- 任何未知都通过"观察→猜测→验证"的流程逐步揭开。

## 5. 审查强制加入
每轮输出前自检：
- 本轮每个 NPC 知道的姓名/身份是否都有合法来源。
- 正文和对白是否泄露了系统设定、后台状态或其他角色内心所想。
- 发现泄露时必须修正为现场可见称呼、主观猜测或错误认知。`

/**
 * 构建防火墙预设条目（可注入到 system prompt 前面）
 */
export function buildFirewallBlock(): string {
  return `\n\n${ROLE_FIREWALL_CONTENT}\n\n---\n`
}

/**
 * 防火墙提示词切片（截短版，给轻量级场景用）
 */
export const ROLE_FIREWALL_SHORT = `【角色认知防火墙】NPC 只知道其亲眼所见/亲耳所闻的信息。不能使用系统设定、后台资料或其他角色内心想法。写出 NPC 对白前必须检查信息获取渠道。`
